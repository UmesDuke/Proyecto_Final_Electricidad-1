    private void printf() {
        Notation notation = null;        
        if (opcionBanda4.isSelected()) {
            if (bandas4.isPrintf()) {
                double tol = bandas4.getTolerance().getScaleTolerance();
                notation   = Notation.determine(bandas4.getOhms()); 
                
                if (tol >= 1) {
                    out.setText(notation.getUnscaleNumber() + notation.getPrefix().getPrefix() + " Ohms " + ((int )tol) + '%');
                } else {
                    out.setText(notation.getUnscaleNumber() + notation.getPrefix().getPrefix() + " Ohms " + tol + '%');
                }
                info.setVisible(true);
            } else {
                info.setVisible(false);
            }
        } else {
            if (opcionBanda5.isSelected()) {
                if (bandas5.isPrintf()) {
                    double tol = bandas5.getTolerance().getScaleTolerance();
                    notation   = Notation.determine(bandas5.getOhms()); 
                    
                    if (tol >= 1) {
                        out.setText(notation.getUnscaleNumber() + notation.getPrefix().getPrefix() + " Ohms " + ((int )tol) + '%');
                    } else {
                        out.setText(notation.getUnscaleNumber() + notation.getPrefix().getPrefix() + " Ohms " + tol + '%');
                    }
                    info.setVisible(true);
                } else {
                    info.setVisible(false);
                }
            }
        }
    }
    
