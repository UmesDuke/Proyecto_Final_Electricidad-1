    /**
     * Metodo encargado de crera el modelo de colores que contendra cada
     * lista desplegable.
     * @param model Tipo de modelo de color.
     * @return Modelo para el ComboBox.
     */
    private DefaultComboBoxModel<JColor> createComboBoxModel(JColorListCellRenderer.TypeComboModel model) {
        switch (model) {
            case COLOR_BANDA:
                return new DefaultComboBoxModel<>(Bandas.BANDA);
            case COLOR_MULTIPLICADOR:
                return new DefaultComboBoxModel<>(Bandas.MULTIPLICADOR);
            case COLOR_TOLERANCIA:
                return new DefaultComboBoxModel<>(Bandas.TOLERANCIA);
            default:
                throw new AssertionError();
        }
    }

