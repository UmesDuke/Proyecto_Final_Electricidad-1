    @Override
    protected void componentesAdd() {
        final JmeProperties jp = Utils.getProperties();        
        jCheckBox1.setSelected(jp.optBoolean("System.exit", true));
        
        switch (jp.optEnum("App.theme", Theme.Type.Linght)) {
            case Dark:
                jComboBox1.setSelectedIndex(1);
                break;
                case Linght:
                jComboBox1.setSelectedIndex(0);
                break;
            default:
                throw new AssertionError();
        }
        jPanel1.setBackground(Theme.getColor("color.dark.panel"));
    }

    @Override
    public void setVisible(boolean visible) {
        super.setVisible(visible);
        if (visible) {
            componentesAdd();
            index = jComboBox1.getSelectedIndex();
            jCheckBox1.requestFocus();
        }
    }
    
    @Override
    public void doClose(int state) {
        //super.doClose();
        final JmeProperties jp = Utils.getProperties();
        int estado = RET_CANCEL;
        
        jp.put("System.exit", jCheckBox1.isSelected());
        if (index != jComboBox1.getSelectedIndex()) {
            switch (jComboBox1.getSelectedIndex()) {
                case 0:
                    jp.put("App.theme", Theme.Type.Linght);
                    break;
                case 1:
                    jp.put("App.theme", Theme.Type.Dark);
                    break;
                default:
                    throw new AssertionError();
            }
            
            Theme.loadTheme();
            estado = RET_OK;
            
            try {
                int res = JOptionPane.showConfirmDialog(null, "Es necesario reiniciar la aplicacion para aplicar los cambios\nDesea aplicarlos los cambios ahora?",
                                                        "Aplicar caambios",
                                                        JOptionPane.OK_CANCEL_OPTION,
                                                        JOptionPane.QUESTION_MESSAGE);
                if (res == JOptionPane.OK_OPTION) {
                    if (resetListener != null) {
                        UIManager.setLookAndFeel(Theme.getNameLookAndFeel());
                        resetListener.closeWindow(this);
                    }
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        
        Utils.save();
        super.doClose(estado);
    }

